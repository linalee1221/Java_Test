package For_If;

public class For {

	public static void main(String[] args) {
	        
		System.out.println("1부터 10까지 출력하기");
		System.out.println("---------------------");
	        for(int i=1;i<=10;i++)
	        {
	            System.out.print(i+" ");
	        }
	        System.out.println("");
		
	}

}
